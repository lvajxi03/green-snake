std::wstring game_titles[6] = {
  L"LEVEL #1",
  L"LEVEL #2",
  L"LEVEL #3",
  L"LEVEL #4",
  L"LEVEL #5",
  L"LEVEL #6"
};

Level::Level(unsigned char index) {
  _spot = NULL;
  _points = 0;
  int i = 0;
  int j = 0;
  _title = game_titles[index];
  switch(index) {
  case 0:
    for (i = 0; i < 40; i++) {
      for (j = 0; j < 20; j++) {
        level[j][i] = level_1[j][i];
      }
    }
    break;
  case 1:
    for (i = 0; i < 40; i++) {
      for (j = 0; j < 20; j++) {
        level[j][i] = level_2[j][i];
      }
    }
    break;
  case 2:
    for (i = 0; i < 40; i++) {
      for (j = 0; j < 20; j++) {
        level[j][i] = level_3[j][i];
      }
    }
    break;
  case 3:
    for (i = 0; i < 40; i++) {
      for (j = 0; j < 20; j++) {
        level[j][i] = level_4[j][i];
      }
    }
    break;
  case 4:
    for (i = 0; i < 40; i++) {
      for (j = 0; j < 20; j++) {
        level[j][i] = level_5[j][i];
      }
    }
    break;
  case 5:
    for (i = 0; i < 40; i++) {
      for (j = 0; j < 20; j++) {
        level[j][i] = level_6[j][i];
      }
    }
    break;
  }

  for (j = 0; j < 20; j++) {
    for (i = 0; i < 40; i++) {
      switch (level[j][i]) {
      case 2:
        _points++;
        break;
      case 3:
        {
          entry_points.push_back(new Point(i, j));
          level[j][i] = 2;
          _points++;
        }
        break;
      case 4:
        _spot = new Point(i, j);
        break;
      case 5:
        // No points for blue pill.
        break;
      case 6:
        _points++;
        break;
      case 7:
        _points++;
        break;
      default:
        break;
      }
    }
  }
}

Level::~Level() {
  delete _spot;
  if (entry_points.size() > 0) {
    std::vector<Point *>::iterator i;
    for (
         i = entry_points.begin();
         i != entry_points.end();
         ++i) {
      Point *p = *i;
      delete p;
    }
    entry_points.clear();
  }
}

unsigned int Level::points() {
  return _points;
}

unsigned int Level::at(unsigned int x, unsigned int y) {
  return level[y][x];
}


std::wstring Level::title() {
  return _title;
}

void Level::clear(unsigned int x, unsigned int y) {
  level[y][x] = 0;
}

Point *Level::spot() {
  return _spot;
}
